class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node *ans = head;
        
        Node *curr = head;
        int len = 0;
        Node *end = head;
        
        while(end->next != NULL){
            end = end->next;
        }
        
        while(len < k){
            Node* newNode = new Node(curr->data);
            end->next = newNode;
            end = end->next;
            ans = ans->next;
            Node *temp = curr;
            curr = curr->next;
            delete temp;
            len++;
        }
        
        return ans;
    }
};
    


